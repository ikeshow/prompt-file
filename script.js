const categories = Array.from(new Set(PROMPTS.map(p => p.category))).sort();

/* ===== DOM参照 ===== */
const nav = document.getElementById('categoryNav');
const list = document.getElementById('promptList');
const tmpl = document.getElementById('promptItemTemplate');
const summary = document.getElementById('promptSummary');
const searchInput = document.getElementById('searchInput');
const themeToggle = document.getElementById('themeToggle');

/* ===== 状態 ===== */
const filterState = { category: null, keyword: '' };

/* ===== カテゴリ描画 ===== */
function renderCategories() {
  nav.innerHTML = '';
  nav.appendChild(createCategoryButton(null, 'すべて'));
  categories.forEach(cat => nav.appendChild(createCategoryButton(cat, cat)));
  if (nav.querySelector('button')) {
    nav.querySelector('button').setAttribute('aria-current', 'true');
  }
}

function createCategoryButton(value, label) {
  const btn = document.createElement('button');
  btn.type = 'button';
  btn.dataset.value = value || '';
  btn.textContent = label;
  btn.addEventListener('click', () => {
    [...nav.querySelectorAll('button')].forEach(b => b.setAttribute('aria-current', b === btn ? 'true' : 'false'));
    filterState.category = value;
    renderList();
  });
  return btn;
}

/* ===== リスト描画 ===== */
function renderList() {
  list.innerHTML = '';
  const kw = filterState.keyword.toLowerCase();
  const data = PROMPTS.filter(p => {
    const catOk = !filterState.category || p.category === filterState.category;
    // 解説(explain)を削除したため、検索対象からも除外
    const kwOk = !kw || [p.title, p.prompt, p.category].some(v => String(v).toLowerCase().includes(kw));
    return catOk && kwOk;
  });
  summary.textContent = `${data.length}件表示 / 全${PROMPTS.length}件`;
  if (!data.length) {
    list.insertAdjacentHTML('beforeend', `<li class="prompt-empty">${list.dataset.emptyMessage}</li>`);
    return;
  }
  const frag = document.createDocumentFragment();
  data.forEach(p => frag.appendChild(buildPromptItem(p)));
  list.appendChild(frag);
}

function buildPromptItem(p) {
  const node = tmpl.content.cloneNode(true);
  const li = node.querySelector('li');
  li.dataset.id = p.id;
  li.querySelector('.prompt-item__title').textContent = p.title;
  li.querySelector('.prompt-item__category').textContent = p.category;
  li.querySelector('.prompt-item__prompt').textContent = p.prompt;

  const promptWrap = li.querySelector('.prompt-item__prompt-wrap');
  const showDetailsBtn = li.querySelector('.btn-show-details');

  showDetailsBtn.addEventListener('click', () => {
    const expanded = showDetailsBtn.getAttribute('aria-expanded') === 'true';
    showDetailsBtn.setAttribute('aria-expanded', String(!expanded));
    promptWrap.hidden = expanded;
    showDetailsBtn.textContent = expanded ? '詳細を見る' : '詳細を隠す';

    if (!expanded) {
      promptWrap.scrollIntoView({ block: 'nearest', behavior: 'smooth' });
    }
  });

  const copyBtn = li.querySelector('.btn-copy');
  copyBtn.addEventListener('click', () => copyPrompt(p, li));

  return node;
}

/* ===== コピー処理 ===== */
async function copyPrompt(p, li) {
  try {
    await navigator.clipboard.writeText(p.prompt);
    showCopyFeedback(li, 'コピーしました');
  } catch (e) {
    fallbackCopy(p.prompt, li);
  }
}

function fallbackCopy(text, li) {
  const ta = document.createElement('textarea');
  ta.value = text;
  document.body.appendChild(ta);
  ta.select();
  try {
    document.execCommand('copy');
    showCopyFeedback(li, 'コピーしました');
  } catch {
    showCopyFeedback(li, 'コピー失敗');
  } finally {
    ta.remove();
  }
}

function showCopyFeedback(li, msg) {
  const fb = li.querySelector('.copy-feedback');
  fb.textContent = msg;
  fb.hidden = false;
  clearTimeout(fb._t);
  fb._t = setTimeout(() => { fb.hidden = true; }, 1800);
}

